/**
 * Articles.
 *
 * Metadata lives here; the body of each one is a markdown file under
 * content/articles/<slug>.md. Markdown is rendered to HTML at build time
 * (this module is server-only), so `marked` never reaches the browser.
 *
 * To add an article: drop a new .md file in content/articles, add an entry
 * below, and generate a thumbnail with scripts/build-article-art.mjs.
 * Order here is the order shown on the homepage — the first is featured.
 */
