export function getArticle(slug: string): Article | undefined {
  return ARTICLES.find((a) => a.slug === slug);
}

/** Thumbnail path for an article, generated by scripts/build-article-art.mjs. */
export function articleImage(slug: string, width: 800 | 1400 = 800) {
  return `/assets/articles/${slug}-${width}`;
}

/** Read the markdown body and render it to HTML. Build-time only. */
export function renderArticleBody(slug: string): string {
  const path = join(process.cwd(), "content", "articles", `${slug}.md`);
  const md = readFileSync(path, "utf8");
  return marked.parse(md, { async: false }) as string;
}
